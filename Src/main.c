int main(void)
{
    uint16_t i, BT_Num;
	uint32_t SUM[3];
	uint16_t ADC_DATA[101];
	uint16_t VREFINT_DATA[101];
	ADC_ChannelConfTypeDef sConfig;
	float V_CHANNEL = 0;
	int16_t ADC_value;

    HAL_Init();

    SystemClock_Config();

    MX_GPIO_Init();
     
    MX_USART1_UART_Init();
    
    MX_ADC_Init();
    
    /* USER CODE BEGIN 2 */
    MX_NFC_Init();
    
    LED_DisPlay();
    
    while (1)
    {              
        if(key_flag[0] == 1)
        {
            /* Default picture display */
            if(key_flag[2] == 1) 
            {
                LED_DisPlay();
                epd1in54_showbmp(nfcBuffer1); 
                key_flag[0] = 0; 
            }
            
            /* Battery percentage display */
            if(key_flag[2] == 2) 
            {
                LED_DisPlay();
                SUM[0] = 0;
                SUM[1] = 0;
                for(i=0; i<100; i++)
                {
                    
                    ADC_DATA[i] = 0;
                  
                    HAL_ADCEx_Calibration_Start(&hadc,ADC_SINGLE_ENDED);
                    
                    hadc.Instance->CHSELR=0;
                   
                    //ADC_IN1
                    sConfig.Channel = ADC_CHANNEL_1;
                   
                    HAL_ADC_ConfigChannel(&hadc, &sConfig);
                  
                    HAL_ADC_Start(&hadc);
                   
                    HAL_ADC_PollForConversion(&hadc,1000);
                 
                    ADC_DATA[i] = HAL_ADC_GetValue(&hadc);
               
                    HAL_ADC_Stop(&hadc);
                    SUM[0] += ADC_DATA[i];
                    
                   
                    VREFINT_DATA[i] = 0;
                    HAL_ADCEx_Calibration_Start(&hadc,ADC_SINGLE_ENDED);
                 
                    hadc.Instance->CHSELR=0;
          
                    //ADC_IN0
                    sConfig.Channel = ADC_CHANNEL_0;
                   
                    HAL_ADC_ConfigChannel(&hadc, &sConfig);
                
                    HAL_ADC_Start(&hadc);
                    
                    HAL_ADC_PollForConversion(&hadc,1000);
               
                    VREFINT_DATA[i] = HAL_ADC_GetValue(&hadc);
                   
                    HAL_ADC_Stop(&hadc);
                    SUM[1] += VREFINT_DATA[i];
                }
               
                //adc voltage average
                ADC_DATA[100] = SUM[0] / 100;
                //printf("ADC_DATA:%d\r\n",ADC_DATA[100]);
                
                //voltage reference average
                VREFINT_DATA[100] = SUM[1] / 100;
                //printf("VREFINT_DATA:%d\r\n",VREFINT_DATA[100]);
                
                /*channelx = 2.5V *ADC_DATAx/(VREFINT_DATA)*/
                V_CHANNEL = (float)(2.5*ADC_DATA[100])/(VREFINT_DATA[100]);
          
                ADC_value = V_CHANNEL * 1000;
                //printf("ADC:%dmv\r\n",ADC_value);
                
                BT_Num = (uint16_t)((1 - ((2105 - (ADC_value)) / (2105 - 1375))) * 100);
                //printf("BT_Num:%d\r\n",BT_Num);
                
                HAL_Delay(500);
                
                if(BT_Num >= 75 && BT_Num <= 100)
                    epd1in54_showbmp(BT_100);
                else if(BT_Num >= 50 && BT_Num <= 75)
                    epd1in54_showbmp(BT_75);
                else if(BT_Num >= 25 && BT_Num <= 50)
                    epd1in54_showbmp(BT_50);
                else if(BT_Num >= 1 && BT_Num <= 25)
                    epd1in54_showbmp(BT_25);
                else 
                {
                    LED_DisPlay();
                    
                    EPD_1IN54_Clear();
                }
                
                key_flag[0] = 0;
                key_flag[1] = 0;               
            }
            
            /* Displays an NFC-transmitted image copied from */
            if(key_flag[2] == 3) 
            {
                LED_DisPlay();

                nfcBuffer2 = (unsigned char*)ADDR_FLASH_PAGE_470;

                epd1in54_showbmp_updown(nfcBuffer2); 
                
                key_flag[0] = 0;
                key_flag[1] = 0;
                key_flag[2] = 0;
            }
        }    
        else 
            MX_NFC_Process();                  
    }

}

